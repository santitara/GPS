
#include "delay.h"
//*****************************************************************************
//*****************************************************************************
void delay_10us_x(unsigned int n)  //function to make a 10 us delay
{
    while (n --)
    {
        int i;

        for (i = 0; i < 20; i++)
            asm volatile ("nop");
    }
}
//*****************************************************************************
//*****************************************************************************
void delay_ms(unsigned int msec)             //function to make a msec ms delay
{
    int i;
    for(i=0;i<msec;i++){
        delay_10us_x(100) ;
    }
}
